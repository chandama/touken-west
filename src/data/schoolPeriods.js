/**
 * Get all unique traditions
 */
export const getTraditions = () => {
  return [...new Set(SCHOOL_PERIODS.map(s => s.tradition))];
};

/**
 * Get schools by tradition
 */
export const getSchoolsByTradition = (tradition) => {
  return SCHOOL_PERIODS.filter(s => s.tradition === tradition);
};

/**
 * Get schools active during a specific year
 */
export const getSchoolsActiveInYear = (year) => {
  return SCHOOL_PERIODS.filter(s => s.startYear <= year && s.endYear >= year);
};

/**
 * Get the period for a given year
 */
export const getPeriodForYear = (year) => {
  return Object.values(PERIODS).find(p => year >= p.startYear && year <= p.endYear);
};
